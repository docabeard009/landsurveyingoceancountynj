/* build-knowledge.mjs — runs during each Netlify deploy. Reads every .html
   page and writes netlify/functions/knowledge-data.mjs (imported by chat.mjs).
   Read-only on your pages. Zero deps. Always exits 0 so it can't break a deploy. */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT  = join(ROOT, "netlify", "functions", "knowledge-data.mjs");
const SKIP = new Set(["404.html", "google", "bingsiteauth"]);
function stripBlock(html, tag){ return html.replace(new RegExp("<"+tag+"\\b[^>]*>[\\s\\S]*?</"+tag+">","gi")," "); }
function pick(re, html){ const m = html.match(re); return m ? m[1].trim() : ""; }
function decode(s){ return String(s).replace(/&amp;/g,"&").replace(/&lt;/g,"<").replace(/&gt;/g,">").replace(/&quot;/g,'"').replace(/&#39;/g,"'").replace(/&nbsp;/g," ").replace(/&mdash;/g,"—").replace(/&ndash;/g,"–").replace(/&rsquo;/g,"'"); }
function extract(html){
  const title = decode(pick(/<title[^>]*>([\s\S]*?)<\/title>/i, html)).replace(/\s*\|\s*Lakeland Surveying.*$/i,"").trim();
  const desc = decode(pick(/<meta\s+name=["']description["']\s+content=["']([\s\S]*?)["']/i, html));
  const heads=[]; const hre=/<h[12][^>]*>([\s\S]*?)<\/h[12]>/gi; let hm;
  while((hm=hre.exec(html))&&heads.length<12){ const t=decode(hm[1].replace(/<[^>]+>/g,"")).trim(); if(t) heads.push(t); }
  let body=html.replace(/<head[\s\S]*?<\/head>/i," ");
  ["script","style","nav","header","footer","svg","form"].forEach(t=>{ body=stripBlock(body,t); });
  body=decode(body.replace(/<[^>]+>/g," ")).replace(/\s+/g," ").trim();
  return { title, desc, headings:heads, text:body.slice(0,900) };
}
function build(){
  const pages=[]; let files=[];
  try{ files=readdirSync(ROOT).filter(f=>f.toLowerCase().endsWith(".html")); }catch(e){ console.error("[knowledge] cannot read root:",e.message); }
  for(const f of files){ const lower=f.toLowerCase(); if([...SKIP].some(s=>lower.startsWith(s))) continue;
    try{ const info=extract(readFileSync(join(ROOT,f),"utf8")); if(!info.title&&!info.text) continue; pages.push({slug:f,...info}); }
    catch(e){ console.error("[knowledge] skip",f,e.message); } }
  const banner="/* AUTO-GENERATED by scripts/build-knowledge.mjs on each deploy. Do not edit by hand. */\n";
  try{ writeFileSync(OUT, banner+"export default "+JSON.stringify(pages)+";\n","utf8"); console.log("[knowledge] indexed "+pages.length+" pages -> "+OUT); }
  catch(e){ console.error("[knowledge] WRITE FAILED (keeping existing index):",e.message); }
}
try{ build(); }catch(e){ console.error("[knowledge] fatal (ignored):",e.message); }
process.exit(0);
