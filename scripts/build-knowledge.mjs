/* build-knowledge.mjs — runs on each Netlify deploy. Indexes every content
   page (root + areas/ + services/) into netlify/functions/knowledge-data.mjs
   so the chatbot auto-learns new/edited pages. Read-only; never fails a deploy. */
import { readFileSync, writeFileSync, readdirSync, statSync, existsSync } from "node:fs";
import { join, dirname, basename } from "node:path";
import { fileURLToPath } from "node:url";
const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT  = join(ROOT, "netlify", "functions", "knowledge-data.mjs");
const SKIP_DIRS = new Set(["node_modules",".git","netlify","scripts","assets",".github"]);
const SKIP_FILE = f => /^(google|bingsiteauth|404)/i.test(f);

function stripBlock(h,t){return h.replace(new RegExp("<"+t+"\\b[^>]*>[\\s\\S]*?</"+t+">","gi")," ");}
function pick(re,h){const m=h.match(re);return m?m[1].trim():"";}
function decode(s){return String(s).replace(/&amp;/g,"&").replace(/&lt;/g,"<").replace(/&gt;/g,">").replace(/&quot;/g,'"').replace(/&#39;/g,"'").replace(/&nbsp;/g," ").replace(/&mdash;/g,"—").replace(/&ndash;/g,"–").replace(/&rsquo;/g,"'");}
function extract(html){
  const title=decode(pick(/<title[^>]*>([\s\S]*?)<\/title>/i,html)).replace(/\s*\|\s*Lakeland Surveying.*$/i,"").trim();
  const desc=decode(pick(/<meta\s+name=["']description["']\s+content=["']([\s\S]*?)["']/i,html));
  const heads=[];const hre=/<h[12][^>]*>([\s\S]*?)<\/h[12]>/gi;let hm;
  while((hm=hre.exec(html))&&heads.length<12){const t=decode(hm[1].replace(/<[^>]+>/g,"")).trim();if(t)heads.push(t);}
  let body=html.replace(/<head[\s\S]*?<\/head>/i," ");
  ["script","style","nav","header","footer","svg","form"].forEach(t=>{body=stripBlock(body,t);});
  body=decode(body.replace(/<[^>]+>/g," ")).replace(/\s+/g," ").trim();
  return {title,desc,headings:heads,text:body.slice(0,900)};
}
function walk(dir,rel=""){
  let out=[];
  let entries=[]; try{entries=readdirSync(dir);}catch(e){return out;}
  for(const e of entries){
    if(SKIP_DIRS.has(e)) continue;
    const p=join(dir,e); let st; try{st=statSync(p);}catch{continue;}
    const r=rel?rel+"/"+e:e;
    if(st.isDirectory()) out=out.concat(walk(p,r));
    else if(e.toLowerCase().endsWith(".html")&&!SKIP_FILE(e)) out.push(r);
  }
  return out;
}
function build(){
  const rels=walk(ROOT);
  const areaBasenames=new Set(rels.filter(r=>r.startsWith("areas/")).map(r=>basename(r)));
  const pages=[];
  for(const rel of rels){
    const base=basename(rel);
    // skip root-level duplicates of an areas/ page (keep the areas/ canonical), but always keep root index.html (homepage)
    if(!rel.includes("/") && base!=="index.html" && areaBasenames.has(base)) continue;
    try{
      const info=extract(readFileSync(join(ROOT,rel),"utf8"));
      if(!info.title && !info.text) continue;
      pages.push({slug:rel,...info});
    }catch(e){console.error("[knowledge] skip",rel,e.message);}
  }
  const banner="/* AUTO-GENERATED by scripts/build-knowledge.mjs on each deploy. Do not edit by hand. */\n";
  try{writeFileSync(OUT,banner+"export default "+JSON.stringify(pages)+";\n","utf8");console.log("[knowledge] indexed "+pages.length+" pages -> "+OUT);}
  catch(e){console.error("[knowledge] WRITE FAILED (keeping existing):",e.message);}
}
try{build();}catch(e){console.error("[knowledge] fatal (ignored):",e.message);}
process.exit(0);
